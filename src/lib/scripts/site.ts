export function DataIndicator(data: any[]) {
    if (data.length > 0) {
        return '*';
    } else {
        return '';
    }
}

export function DataIndicatorArray(data: any) {
    let count = 0
    for (const [key, value] of Object.entries(data)) {
        count += value.length;
    }
    if (count > 0) {
        return '*';
    } else {
        return '';
    }
}


